export const formatCurrency = (v: number | null): string => {
  if (v === null || isNaN(v)) return '—';
  return new Intl.NumberFormat('en-US', { style: 'currency', currency: 'USD' }).format(v);
};

export const formatPercent = (v: number | null): string => {
  if (v === null || isNaN(v)) return '—';
  return v.toFixed(1) + '%';
};

export const parseNumericInput = (value: string | null): number | null => {
  const normalized = String(value ?? "").trim().replace(/[$,\s]/g, "");
  if (!normalized) return null;
  if (!/^-?\d*\.?\d+$/.test(normalized)) return null;
  const parsed = Number(normalized);
  return Number.isFinite(parsed) ? parsed : null;
};

export const scoreColor = (score: number): string => {
  if (score >= 65) return THEME.green;
  if (score >= 50) return THEME.yellow;
  return THEME.red;
};

